        <div class="symptom">
            <h3 class="definition">Peut-on guérir de dysphasie et traitements</h3>
            <p>
                On ne peut pas guérir de ce trouble, mais une rééducation orthophonique est nécessaire.
                Le suivi orthophonique peut également être complété par d'autres spécialiste, tels que psychomotricien,
                ergothérapeute et psychologue.
            </p>
            <p>
                Pour aider ces enfants, il faut éviter de leur faire répéter les choses, mais plutôt de les aider à
                préciser leurs pensées à travers des schémas, des dessins.
                Il est important de l'encourager, et de lui dire ses progrès. Il faut veiller que lors de la communication,
                l'enfant est disponible, à l'écoute afin de pouvoir attirer son attention visuel, parlez lui doucement.
            </p>
        </div>
