    <div class="container-definition">
        <h2 class="definition">Définition de la Dysphasie (<span class="website">Site Passport Santé</span>)</h2>
        <p>
            La dysphasie ou trouble primaire du langage oral est un trouble neurodéveloppemental du langage oral.
            Ce trouble entraîne un déficit sévère et durable du développement de la production et/ou de la
            compréhension de la parole et du langage. Ce trouble qui débute dès la naissance est présent tout au long
            de la vie, de manière plus ou moins prononcée selon la prise en charge durant l’enfance.
        </p>
    </div>

    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">Symptômes</h3>
            <p>
                Accompagné de plusieurs symptômes, la dysphasie peut toucher un seul ou bien plusieurs domaines comme:
                des troubles de l'expression orale avec difficultés de prononciation, une difficulté à associer à la fois
                les mots et les gestes, des difficultés à prononcer le bon mot. Mais encore les notions d'espace et de
                temps sont difficile à comprendre.
                La mémoire verbale immédiate est mauvaise, les difficultés en orthographe sont importante.
            </p>
            <p>On retrouvera un trouble de l'expression, de la réception et de syntaxe.</p>
        </div>
