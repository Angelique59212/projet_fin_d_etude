    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">A l'école</h3>
            <p>
                La présence d'une auxiliaire de vie(AVS) est indispensable pour accompagner l'enfant, elle lui permet
                de pouvoir l'aider à prendre ses notes, à lui photocopier les cours, à l'aider à comprendre ce qu'il n'a
                pas compris. Elle peut aller plus lentement.
            </p>
            <p>
                L'enfant peut bénéficier de plus de temps que les autres, pour effectuer ses tâches. Le maître peut lui
                faire reformuler un énoncé, afin de s'assurer que l'élève est bien compris.
                Le mâitre peut également et il est même recommandé de diviser par deux le travail à effectuer à la maison.
                Il est important d'éviter les punitions, le recopiage d'un cours mal écrit afin que l'élève ne se décourage
                pas, il faut l'encourager, et lui dire ses progrès.
            </p>
            <p>
                Le maître peut également utiliser des codes couleurs, dans les leçons, comme pour les cahiers.
                En ce qui concerne la poésie, il est maintenant courant que les élèves n'ont pas tous la même poésie à
                apprendre selon les écoles, encourager l'enfant à ne pas prendre la plus longue.
                Si il est possible, et que l'enfant a été guidé sur comment l'utiliser, il peut être pratique de faire
                recours à l'ordinateur, qui peut être une aide précieuse.
            </p>
        </div>
