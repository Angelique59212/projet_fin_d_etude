    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">Suivi d'un enfant Dysgraphique</h3>
            <p>
                Le traitement débute tout d'abord d'un suivi orthophonique, mais elle est aussi pluridisciplinaire: on
                peut faire appel à un pédiatre, neurologue, ergothérapeute, psychomotricien.
                Le suivi commence par apprendre au patient de se relaxer afin de maîtriser ses gestes.
                On travaillera également sur ses points faibles comme sa posture, la motricité fine mais encore de la
                tenue du stylo.
                On privilégira la qualité à la quantité de ses écrits, l'encouragement.
            </p>
