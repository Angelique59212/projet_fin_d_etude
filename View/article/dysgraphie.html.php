    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">A l'école</h3>
            <p>
                Il est important de limiter la quantités d'écriture, on peut éviter de lui faire écrire les consignes,
                on peut utiliser des photocopiés des leçons, ou de photocopier les cours des camarades.
                En ce qui concerne les dictées, on peut lui proposer des dictées à trous dans laquel il n'aurait plus qu'à
                écrire les mots les plus importants.
                On peut privilégier une participation à l'oral y compris lorsqu'il s'agit des évalutations.
                Ces élèves ont besoin d'un accompagnement scolaire pour les accompagner à la réussite, comme une Auxiliaire
                de Vie Scolaire (AVS).
            </p>
        </div>
