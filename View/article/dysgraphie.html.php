    <div class="container-definition">
        <h2 class="definition">Définition de la Dysgraphie(<span class="website">Site PassportSante</span>)</h2>
        <p>
            La dysgraphie est donc un trouble persistant de la réalisation du geste graphique, affectant la forme de
            l'écriture, mais aussi sa rapidité d'exécution.
            Elle peut notamment faire partie de la symptomatologie des troubles de la proprioception : soit, la capacité
            à déterminer la position des parties du corps, ainsi que l'amplitude ou la direction de ses mouvements,
            sans l'appui d'indices visuels ou auditifs.
        </p>
    </div>

    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">Symptômes</h3>
            <p>
                Les dysgraphique ont une lenteur au niveau de l'écriture, et lorsqu'il faut trop écrire, son écriture en
                devient illisible. Il oubliera de mettre les signes de ponctuation, il omettra également des lettres dans
                ses mots.
                Il est compliqué pour ses élèves d'écouter en même temps que d'écrire.
                Il sera inutile de lui demander de s'entraîner à écrire car cela pourrait avoir l'effet inverse.
                L'enfant manquera de confiance en lui, et pourra même être en phobie scolaire si il se décourage, il
                pourrait être amené à refuser d'écrire.
                Réaliser plusieurs tâches sera impossible étant donné l'effort que lui demande déjà l'écriture.
            </p>
        </div>
