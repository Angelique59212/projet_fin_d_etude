    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">A l'école</h3>
            <p>
                On peut utiliser des penses-bêtes pour éviter les confusions. Il faut favoriser les exercices à trous et
                ne pas pénaliser l'orthographe, mais noter le nombre de mots correctement orthographiés qui permettra de
                mettre les efforts de l'élève en exergue.
                Il est conseillé de lui réduire la quantité de travail surtout en ce qui concerne le nomnbre de mots à
                apprendre.
                On peut lui imprimer les schémas en format A3, lui photocopier les cours, et même faire la demande d'une
                Auxiliaire de Vie Scolaire (AVS). Un ordinateur peut aussi lui apporter de l'aide pour sa prise de notes.
                il peut être intéressant de lui attribuer un cache-lecture qui lui permettra de ne pas se perdre dans la 
                lecture afin de ne visualiser qu'une ligne à la fois.
            </p>
        </div>
        <div class="image-symptom">
            <img src="/assets/img/dysorthographie/dysorthographie-enfant.jpg" alt=" dysortographie aide ecole">
        </div>
    </div>

    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">Accompagnement de l'enfant</h3>
            <p>
                On ne peut guérir totalement de la dysorthographie, en revanche on peut l'aider à s'améliorer sur ses
                points faible et d'atténuer ses symptômes.
                Une rééducation orthophonique est nécessaire, une aide de moyens mémotechnique lui sera donné ainsi que
                pour la visualisation.
                L'orthophiniste peut être associé à un psychomotricien et graphothérapeute.
                Le but de l'orthophoniste est de revoir les bases de la conjugaison et l'amener à se poser des questions
                grâce à des repères.
                Un suivi psychologique peut être nécessaire si l'enfant est en souffrance face à son trouble, et si il 
                manque trop de confiance en lui.
            </p>
        </div>
