    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">A l'école</h3>
            <p>
                Tout d'abord les parents peuvent aider leur enfant dans la vie de tout les jours avec de petites choses
                simple à mettre en place comme combien as tu eu de bonbons, combien y a t-il de bananes dans la corbeille?
                Ranger ses jouets du plus petit au plus grand, jouer à un jeu ou il faut compter l'avancement des pions
                comme le jeu de dada.
            </p>
            <p>
                L'enseignant peut laisser à la disponibilité de l'élève une bande numérique pour qu'il puisse y placer les
                éléments. Il faut privilégier les objets en dénombrements plutôt que sur feuille qui lui permettra de mieux
                se rendre compte en manipulant.
                Utiliser des codes couleurs pour différencier les centaines, dizaines, unités.
                On peut aussi utiliser un timer pour lui permettre de gérer son temps.
            </p>
        </div>
