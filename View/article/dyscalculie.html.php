    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">Guérison et Accompagnement</h3>
            <p>
                Ce trouble ne se guérit pas mais des aménagements peuvent être mis en place. Une rééducation orthophonique
                est nécessaire. L'orthophoniste l'accompagnera dans le raisonnement mathématique à l'aide de mémos, et peut
                également lui apprendre l'utilisation de la calculatrice.
                Elle peut faire appel également à d'autres spécialiste tels qu'un orthoptiste ou psychomotricien s'il présente
                un manque de repère spatio-temporelle.
                Une prise en charge avec une AVS est nécessaire en classe.
                Un recours à un orthopédagogue peut aussi être utile de manière à travailler de manière individuel pour lui
                permettre de devenir autonome.
            </p>
        </div>
