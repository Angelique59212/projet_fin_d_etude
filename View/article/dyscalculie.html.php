        <h2 class="definition">
            Définition de la Dyscalculie (<span class="website">Site institut des troubles d'apprentissage</span>)
        </h2>
        <p>
            Le trouble d’apprentissage en mathématiques est aussi souvent appelé «dyscalculie»1. Il est un trouble
            spécifique d’apprentissage qui touche les mathématiques et affecte l’apprentissage dès les premières années
            de scolarité. Les difficultés sont durables et résistent aux interventions mises en place pour aider le jeune.
            Toutefois, avec l’aide qui lui convient, même un enfant qui présente un trouble d’apprentissage en
            mathématiques peut apprendre les mathématiques et parfois compenser ses difficultés avec des outils adéquats.
        </p>
    </div>

    <div class="container-second">
        <div class="symptom">
            <h3 class="definition">Symptômes</h3>
            <p>
                L'enfant dyscalculique aura du mal à retenir les nombres, voir même à inverser les chiffres. Il comptera
                sur ses doigts, il aura des difficultés à retenir à la fois ses tables d'addition et de multiplication.
                Il ne comprendra pas les termes mathématique, il aura du mal à quantifier, se trouvera en difficultés face
                à des problèmes à réaliser.
                Lire l'heure lui sera également compliqué. Il aura un mauvais sens de l'orientation et aura du mal à
                s'organiser.
                Attention ces personnes ne sont pas nul en mathématique car elles excellent en géométrie et en physique
            </p>
        </div>
